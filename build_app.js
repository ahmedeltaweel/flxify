const fs = require('fs');
const path = require('path');

const SCRIPTS_DIR = path.join(__dirname, 'scripts');
const LIB_DIR = path.join(SCRIPTS_DIR, 'lib');
const OUTPUT = path.join(__dirname, 'app.js');

// ---- Auto-discover lib modules ----
const libFiles = fs.readdirSync(LIB_DIR)
  .filter(f => f.endsWith('.js'))
  .sort();

// ---- Auto-discover scripts ----
const scriptFiles = fs.readdirSync(SCRIPTS_DIR)
  .filter(f => f.endsWith('.js'))
  .sort();

// ---- Parse script metadata at build time ----
function parseMetadata(source, filename) {
  const metaMatch = source.match(/\/\*\*([\s\S]*?)\*\*\//);
  if (!metaMatch) {
    console.warn(`  WARNING: No metadata block in ${filename}, skipping`);
    return null;
  }
  const cleanedJson = metaMatch[1].trim().replace(/,\s*([\]}])/g, '$1');
  try {
    return JSON.parse(cleanedJson);
  } catch (e) {
    console.warn(`  WARNING: Invalid metadata JSON in ${filename}: ${e.message}`);
    return null;
  }
}

// ---- Read and validate all scripts ----
const scriptEntries = [];
for (const file of scriptFiles) {
  const source = fs.readFileSync(path.join(SCRIPTS_DIR, file), 'utf-8');
  const metadata = parseMetadata(source, file);
  if (!metadata) continue;
  scriptEntries.push({ file, source, metadata });
}

// ---- Read lib modules ----
const libEntries = [];
for (const file of libFiles) {
  const source = fs.readFileSync(path.join(LIB_DIR, file), 'utf-8');
  const name = file.replace(/\.js$/, '');
  libEntries.push({ file, name, source });
}

console.log(`Found ${libEntries.length} lib modules and ${scriptEntries.length} scripts.`);

