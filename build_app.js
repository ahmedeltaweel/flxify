function fuzzyScore(query, text) {
  if (!query) return 1;
  var lowerQuery = query.toLowerCase();
  var lowerText = text.toLowerCase();

  if (lowerText === lowerQuery) return 100;
  if (lowerText.startsWith(lowerQuery)) return 80;
  if (lowerText.includes(lowerQuery)) return 60;

  var qi = 0;
  var consecutive = 0;
  var maxConsecutive = 0;
  var matched = 0;
  for (var ti = 0; ti < lowerText.length && qi < lowerQuery.length; ti++) {
    if (lowerText[ti] === lowerQuery[qi]) {
      qi++;
      matched++;
      consecutive++;
      if (consecutive > maxConsecutive) maxConsecutive = consecutive;
    } else {
      consecutive = 0;
    }
  }
  if (qi < lowerQuery.length) return 0;
  return 20 + maxConsecutive * 5 + matched;
}

function filterScripts(query) {
  if (!query) {
    filteredScripts = scripts.slice();
  } else {
    var scored = scripts.map(function(s) {
      var nameScore = fuzzyScore(query, s.name) * 0.9;
      var tagScore = fuzzyScore(query, s.tags) * 0.6;
      var descScore = fuzzyScore(query, s.description) * 0.2;
      var score = Math.max(nameScore, tagScore, descScore);
      return { script: s, score: score };
    }).filter(function(s) { return s.score > 0; });
    scored.sort(function(a, b) { return b.score - a.score; });
    filteredScripts = scored.map(function(s) { return s.script; });
  }
  activeIndex = 0;
  renderResults();
}

function renderResults() {
  resultsList.innerHTML = '';
  filteredScripts.forEach(function(script, index) {
    var li = document.createElement('li');
    if (index === activeIndex) li.className = 'active';

    var nameSpan = document.createElement('span');
    nameSpan.className = 'script-name';
    nameSpan.textContent = script.name;

    var descSpan = document.createElement('span');
    descSpan.className = 'script-desc';
    descSpan.textContent = script.description;

    li.appendChild(nameSpan);
    li.appendChild(descSpan);

    li.addEventListener('click', function() {
      executeScript(script);
      hidePalette();
    });

    resultsList.appendChild(li);
  });

  var activeEl = resultsList.querySelector('.active');
  if (activeEl) activeEl.scrollIntoView({ block: 'nearest' });
}

